#include "MS51_32K.H"


//----------------------------------------------------------------------------------------------//
void main (void)
{
  ALL_GPIO_QUASI_MODE;
  while(1)
  {
    P1 = 0xff;
    P2 = 0xFF;
    P3 =0xFF;
    P0 =0xFF;
    Timer0_Delay(16000000,100,1000);;
    P1 = 0x00;
    P2 = 0x00;
    P3 =0x00;
    P0 =0x00;
    Timer0_Delay(16000000,100,1000);;
  }
}



